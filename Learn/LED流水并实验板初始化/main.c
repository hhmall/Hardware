/*********************************************************************************
*实验名   ：流水灯
*实验效果	：流水灯（循环位移法）
*
*********************************************************************************/
#include <reg52.h>
#include <intrins.h>
#include "delay.h"
#include "bstv51.h"


void main()
{
	bstv51_init();
    P1=0Xfe;//1111 1110
    while(1)
	{
	    delay(200);
		P1=_crol_(P1,1);     //循环移位函数 把P1寄存器内的数向左移1位
	}
}